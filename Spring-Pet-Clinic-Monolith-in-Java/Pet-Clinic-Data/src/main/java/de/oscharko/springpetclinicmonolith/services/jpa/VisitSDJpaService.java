package de.oscharko.springpetclinicmonolith.services.jpa;

import de.oscharko.springpetclinicmonolith.model.Visit;
import de.oscharko.springpetclinicmonolith.repositories.VisitRepository;
import de.oscharko.springpetclinicmonolith.services.VisitService;
import org.springframework.context.annotation.Profile;
import org.springframework.stereotype.Service;

import java.util.HashSet;
import java.util.Set;

